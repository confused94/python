import turtle
import time
def kare(renk):
    turtle.pencolor(renk)
    turtle.home()

    for i in range(4):
        turtle.forward(100)
        time.sleep(2)
        turtle.left(90)
        time.sleep(2)
        turtle.done
        
kare("red")             

